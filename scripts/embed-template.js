#!/usr/bin/env node
/**
 * Post-build script: converts frontend/dist/index.html into
 * danmu_api/ui/template.js so the backend can import it as a string.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const projectRoot = path.resolve(__dirname, "..");
const distHtmlPath = path.join(projectRoot, "frontend", "dist", "index.html");
const templateOutputPath = path.join(projectRoot, "danmu_api", "ui", "template.js");

if (!fs.existsSync(distHtmlPath)) {
  console.error(`[embed-template] Error: ${distHtmlPath} not found. Run "npm run build:ui" first.`);
  process.exit(1);
}

const html = fs.readFileSync(distHtmlPath, "utf-8");

// Escape characters that would break a JS template literal.
const escaped = html
  .replace(/\\/g, "\\\\")
  .replace(/`/g, "\\`")
  .replace(/\$/g, "\\$");

const output = `// Auto-generated by scripts/embed-template.js
// Do not edit manually.
export const HTML_TEMPLATE = \`${escaped}\`;
`;

fs.writeFileSync(templateOutputPath, output, "utf-8");
console.log(`[embed-template] Wrote ${templateOutputPath} (${output.length} bytes)`);

// Sanity checks.
const externalScript = /<script[^>]+src\s*=/.test(html);
const externalStyle = /<link[^>]+rel\s*=\s*["']stylesheet["']/.test(html);
if (externalScript || externalStyle) {
  console.warn("[embed-template] Warning: generated HTML still contains external asset references.");
}

const tokenCount = (html.match(/__DANMU_API_TOKEN_VALUE__/g) || []).length;
if (tokenCount !== 1) {
  console.warn(`[embed-template] Warning: expected exactly 1 token placeholder, found ${tokenCount}.`);
}
